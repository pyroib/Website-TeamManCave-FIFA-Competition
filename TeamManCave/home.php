			<h2>How to play</h2>
				
			 <li>You will be required to arrange your own friendly online Vs. match at a mutual agreed time during
					the week of the game. Using your assigned teams.</li>
				
				<li>If the game has not been played OR the organisers have not been informed before Tuesday midnight, 
					then it will be sim'd by one of the organisers.</li>
				
				<li>If you can not get in contact with your opponent, or they have failed to arrange a time to play before 
					tuesday, then you can consider them as a simulation round.</li>
					
				<li>To avoid any claims of cheating, this game can ONLY be played on Tuesday and you MUST post the 
						event page when you are playing the game.</li>
					
				<li>The fixture lists a <img src="/images/ps4.png"> icon next to your team, clicking it will indicate you 
						do not intend on playing the game that week and that you are O.K. for it to be simulated.</li>
				
				<li>The fixture list will display a <img src="/images/computer.png"> icon next to all teams that have been 
					selected to sim the match. If your opponent displays this symbol, you can play the game Vs the computer.</li>
				
				<li>If you are playing a sim'd game, please post in the events page when you are playing it to avoid conflicts.</li>
				
			</ul>
			
			<h3 >You will have a 7 day window to play your game</h3>
            <ul>
                <li>You will have from the beginning of WEDNESDAY to the end of the following TUESDAY to play the game &amp;
                    submit results.</li>

                <li>It is the HOME team players responsibility to organise the game. If the home team has not had a response
                    by the beginning of Tuesday, they can play Vs the CPU. You will need to prove you've
                    tried to organise the match during the week. (2 attempts should be enough, please post it the facebook
                    group, tag your opponent)</li>

                <li>If the away team has not been contacted by the end of Monday, they may themselve contact the home team.
                    If a response is not made by the start of Tuesday, they may play the Game Vs. the CPU. You will
                    need to provide proof that you have tried to contact the home team player on monday.(please post it the
                    facebook group, tag your opponent)</li>
            </ul>



