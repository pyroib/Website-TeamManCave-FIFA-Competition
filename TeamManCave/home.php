			<h3>Fixtures</h3>
			<ul>
				<li>If both players are going to be at the ManCave at some point that week. The game 
					<strong>MUST</strong> be played a the ManCave.</li>
				
				<li>If you have more than 1 team, you can <strong>NOT</strong> play the game when your teams Vs. each other. It 
					<strong>WILL</strong> be simmed.</li>			
			</ul>
			
			<h3>The Table</h3>
			<ul>
				<li>3 Points for a win. 1 point for a draw. 0 for a lose</li>
				
				<li>Table order will be Points, For/against, least against, Most for. If need be, simmed games will have away 
					goals rules apply</li>
			</ul>
			
			<h3>Game settings</h3>
			<ul>
				<li>PS4 <strong>ONLY</strong></li>
				<li>The home team in the fixtures will be player 1 in the game</li>
				<li>All games are to be played using live lineups</li>
				<li>All games are to be played in the home teams stadium</li>
				<li>All games are to be played in semi-pro settings</li>
				<li>All games are to be played as 6 min halves</li>
